package Operators;

public class ArithmeticOperators {

    public static void main(String[] args) {


        //Arithmetic Operators
        int a = 10;
        int b = 20;
        System.out.println(a + b);
        System.out.println(b - a);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
    }
}
